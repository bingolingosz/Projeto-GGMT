            <h2> História e Jogabilidade </h2>

            <p>Em The Pedestrian, o jogador controla um pequeno boneco de sinalização que ganha vida dentro de placas de
                trânsito, outdoors e outras superfícies urbanas. Sem diálogos ou textos explicativos, a narrativa é
                contada visualmente, conforme o personagem se move entre os sinais e descobre conexões inesperadas no
                mundo real. Aos poucos, o jogo sugere uma história maior por trás de sua premissa aparentemente simples,
                envolvendo a interação entre o ambiente e a jornada do protagonista.

                A jogabilidade se baseia na resolução de quebra-cabeças criativos, onde o jogador deve reorganizar e
                conectar placas para criar caminhos contínuos entre elas. O objetivo é encontrar a melhor maneira de
                atravessar os cenários, manipulando escadas, portas, alavancas e circuitos elétricos para abrir novas
                rotas. Conforme o jogo avança, os desafios se tornam mais complexos, exigindo lógica e experimentação
                para desvendar suas soluções.</p>
